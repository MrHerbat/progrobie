package Player;

import Armory.Armors;
import Armory.Weapons;

public class Equipment
{
    public Weapons mainHand;
    public Armors armor;
    public Weapons weaponOffHand;
    public Armors armorsOffHand;

    public Equipment() {
        this.mainHand = mainHand;
        this.armor = armor;
        this.weaponOffHand = weaponOffHand;
        this.armorsOffHand = armorsOffHand;
    }
}
