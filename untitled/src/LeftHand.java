public class LeftHand
{
    Weapon weapon;
    Armory equippedWeapon;

    public LeftHand()
    {
        this.weapon = weapon;
        this.equippedWeapon = equippedWeapon;
    }

    public void Equip(Armory equippedWeapon)
    {
        this.equippedWeapon = equippedWeapon;
    }
}
