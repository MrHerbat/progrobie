public class Bullet
{
    double diameter;
    double weight;
    double height;
    String contour;
    String face;
    String materialType;

    public Bullet(double diameter, double weight, double height, String contour, String face, String materialType) {
        this.diameter = diameter;
        this.weight = weight;
        this.height = height;
        this.contour = contour;
        this.face = face;
        this.materialType = materialType;
    }
}
