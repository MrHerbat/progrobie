public class Albums {
    String artist;
    String album;
    int songsNumber;
    int year;
    long downloadNumber;

    public Albums(String artist, String album, int songsNumber, int year, long downloadNumber) {
        this.artist = artist;
        this.album = album;
        this.songsNumber = songsNumber;
        this.year = year;
        this.downloadNumber = downloadNumber;
    }
}
